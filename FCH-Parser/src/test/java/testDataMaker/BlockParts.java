package testDataMaker;

public class BlockParts {
	private byte[] b8 = new byte[8];
	private byte[] b80 = new byte[8];
	private byte[] body;
	private int lenght;
	
	
	public byte[] getB8() {
		return b8;
	}
	public void setB8(byte[] b8) {
		this.b8 = b8;
	}
	public byte[] getB80() {
		return b80;
	}
	public void setB80(byte[] b80) {
		this.b80 = b80;
	}
	public byte[] getBody() {
		return body;
	}
	public void setBody(byte[] body) {
		this.body = body;
	}
	public int getLength() {
		return lenght;
	}
	public void setLenght(int lenght) {
		this.lenght = lenght;
	}
	
	
}
